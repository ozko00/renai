# CSVに変換
import pandas as pd

# データをリスト化
papers_list = []
for category, papers in papers_data.items():
    for paper in papers:
        papers_list.append({
            "Category": category,
            "Title": paper["title"],
            "Year": paper["year"],
            "Type": paper["type"],
            "Key Finding": paper["key_finding"],
            "Source ID": paper["source_id"]
        })

df = pd.DataFrame(papers_list)
df_sorted = df.sort_values(by=["Category", "Year"], ascending=[True, False])

# CSVに保存
csv_content = df_sorted.to_csv(index=False)
print("論文一覧（CSVフォーマット）:")
print(csv_content)

